#include <iostream>
#include <string>

int main() {
    int integerVal = 10;
    double doubleVal = 20.5;
    char charVal = 'A';
    std::string stringVal = "Hello, C++!";

    std::cout << "Integer: " << integerVal << std::endl;
    std::cout << "Double: " << doubleVal << std::endl;
    std::cout << "Character: " << charVal << std::endl;
    std::cout << "String: " << stringVal << std::endl;

    return 0;
}
